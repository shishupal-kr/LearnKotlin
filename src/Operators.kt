fun main(){
    var a = 12
    var b = 5

    println("Arithmetic operators")
    println(a+b)
    println(a-b)
    println(a*b)
    println(a/b)
    println(a%b)

    println("unary operators")
    println(a++)
    println(a--)
    println(++a)
    println(--a)

    print("operators")
    println(a>b)
    println(a<b)
    println(a==b)
    println(a!=b)
    println(a>=b)
    println(a<=b)

    println( "logical operators")
    println(a>b && a<b)  //and
    println(a>b || a<b)  //or

}