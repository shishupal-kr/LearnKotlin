fun main(){


    //While loop
    var i = 0
    while (i <= 5){
        println(i)
        i++
    }

    //2nd example
    var j = 0
    while (j <= 3){
        println("Shishupal")
        j ++
    }


    //do while loop , it will execute at least once before checking the condition after running the loop
    var k = 0
    do {
        println(k)
        k++
    } while (k <= 5)
}